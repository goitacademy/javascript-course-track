/**
 * Operatori
 *
 * - Operatori de comparație
 * - Operatori de egalitate și inegalitate
 * - Convertirea tipurilor operandului
 */

console.log(5 > 4);

console.log(10 >= "7");

console.log("2" > "12");

console.log("2" < "12");

console.log("4" == 4);

console.log("6" === 6);

console.log("false" === false);

console.log(1 == true);

console.log(1 === true);

console.log("0" == false);

console.log("0" === false);

console.log("Papaya" < "papaya");

console.log("Papaya" === "papaya");

console.log(undefined == null);

console.log(undefined === null);
